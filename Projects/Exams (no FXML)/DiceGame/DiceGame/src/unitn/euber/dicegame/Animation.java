package unitn.euber.dicegame;

/**
 * Enumeration of the types of animation
 * @author Eugenio Vinicio Berretta
 */
public enum Animation {
    /**
     * The dices will dissolve when removed from the field
     */
    DISSOLVE,

    /**
     * The dices will traslate when removed from the field
     */
    TRASLATE
}
